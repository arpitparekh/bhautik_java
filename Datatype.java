public class Datatype {

  public static void main(String[] args) {

    // datatype
    // 8 primitive datatype

    // numeric datatypes

    byte b = 127; // 1 bytes  // 8 bit

    short s = 32767; // 2 bytes // 16 bits

    int i = 2147438647; // 4 bytes // 32 bits

    long l = 9223372036854775807L; // 8 bytes // 64 bits

    // decimal datatypes

    float f = 121.343F; // 4 bytes // 32 bits

    double d = 121.3454646543; // 8 bytes // 64 bits


    // character datatype

    char c = '#'; // 2 bytes // 16 bits

    // boolean datatype

    boolean b1 = false; // 1 bit

    System.out.println(b);
    System.out.println(s);
    System.out.println(i);
    System.out.println(l);
    System.out.println(f);
    System.out.println(d);
    System.out.println(c);
    System.out.println(b1);

  }

}
